package dac;

public class PatternDemo3 {

	public static void main(String[] args) {
		int i,j,n=5,m=5;
		//outer for loop
		for(i=1;i<=n;i++)
	       {
	    	//inner for loop
	    	   for(j=1;j<=m;j++)
	    	   {
	    		   System.out.print(" * ");
	    		
	           }
	    	   System.out.println( );
	    	   
	    		  m--;
	    	  
		   }
	}

}
