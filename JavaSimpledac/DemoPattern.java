package dac;


public class DemoPattern {

	public static void main(String[] args) {
		int i,j,r=5,x=5;
		
       for(i=1;i<=r;i++)
       {
    	  x=i;
    	   for(j=1;j<=x;j++)
    	   {
    		   System.out.print(" "+x);
    		   x++;
    		  
    	  
           }
    	   System.out.println( );
	   }


	}

}
